
myNameList = ['John', 'Nancy', 'Mia']
print('If Element is in the list')
if 'John' in myNameList:
    print('Yes, it is in the list')
else:
    print('No, it is not in the list')


print('\nIf Element is not in the list')
if 'John' not in myNameList:
    print('Yes, it is not in the list')
else:
    print('No, it is in the list')


